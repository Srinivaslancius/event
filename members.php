											<p class="noo-team-ds">
												
											    Situated almost midway down the east coast of the Qatari peninsula,
											    Doha is the country's center of administration, finance, culture,
											    transportation, and social services. The modern city grew from the
											    fishing and pearling port of al-Bida, which at the end of the
											    nineteenth century had around 12,000 inhabitants. The town's economy
											    depended to a large extent on pearling, and the busy port had some 300
											    pearling ships in 1939, just before the industry collapsed. After oil
											    revenues began enriching the emirate in the 1960s, the city grew
											    rapidly. Its simple one- and two-story stone, mud, coral block, and
											    timber dwellings were replaced by high-rise apartments and offices,
											    palatial villas, and tree-lined subdivisions supported by modern
											    infrastructure. The city's waterfront is lined by a gracefully curving
											    roadway and landscaped walkway, or corniche. Although the oil and gas
											    industry dominate the local economy, fishing and trade also bring
											    activity to the port town. According to the 1997 census the city had
											    264,009 inhabitants. Because most of the city's residents are
											    non-Qataris, the character of the city resembles others in the Persian
											    Gulf [2] where there are large numbers of Iranians, Indians,
											    Pakistanis, Filipinos, and Bangladeshis who influence the types of
											    restaurants and the items sold in the markets.

											    DOHA SIGHTSEEING [3]

											    Beautiful buildings mingled with the gorgeous green spaces of Aspire
											    Park and MIA Park, up-and-coming cultural sights such as the Museum of
											    Islamic Art and The Katara Cultural Village alongside historic
											    hotspots of the Souq Waqif… Qatar’s capital city is a vibrant hub
											    of activity to satisfy everyone’s needs.

											    Doha is striking, modern and growing rapidly, with some of the
											    world’s largest energy reserves making the country a regional
											    powerhouse. Its strategic position in the Arabian Gulf reinforces the
											    importance of its port, whilst it is also a vital centre for commerce
											    and communication. Business aside, Doha offers lots to explore whether
											    it be taking in the view from The Corniche, spending time at the State
											    Grand and Katara Mosques or visiting The Pearl-Qatar, an artificial
											    island home to marinas, shops and residences.

											    The official language of Qatar is Arabic, but English is widely spoken
											    and understood. Qatar’s shops are open from Saturday to Thursday
											    between the hours of 8:30 am to 12:30 pm, closing briefly before
											    reopening at 4:00 pm, and then closing for the day at 9:00 pm.
											    Shopping malls operate between 10:00 am and 10:00 pm. Some shops are
											    closed on Fridays; however others are open at 4:00 pm. The majority of
											    shopping malls and supermarkets operate extended Friday shopping.


